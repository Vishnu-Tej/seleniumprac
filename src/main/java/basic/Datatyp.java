package basic;

public class Datatyp {

	public static void main(String[] args) {
		int a =10;
		//int a=20;//duplicate variables are not allowed.
		 a=30;
		 System.out.println(a);
		 
	//size of int = 4 bytes. 1 byte = 8 bits.1 byte range =-128 to 127.
		 //byte = 1 byte; short = 2 bytes; int = 4 bytes;long = 8 bytes 
		 
		 //float= 4 bytes;
		 float f =22.2f; // for float we need to write "f" after decimal number.
		 //double =8 bytes;
		 double d= 222.12;
		 System.out.println(f+d);
		 //char = 2bytes;
		 char c ='V';
		 System.out.println(c);
		 //boolean size is around 1 byte.
		 boolean b = true;
				 
		 String s ="vishnu tej";
		 System.out.println(b+s);
		//all the above data types are called primitive data types except string
		 //primitive means Straight forward memory allocation
		 //String is stored as an object.
		 
		 
		 

	}

}
